# -*- coding: utf-8 -*-
# 집합 자료형(set)
# 합집합 : |
# 메소드: set.union()
#%%
s1 = set('123456') 
s2 = set('456789') 
print(s1)
print(s2)

# 양쪽 모두 존재하는 값 선택
# 중복되는 요소는 하나만 선택 
# 중복되는 요소 : 4,5,6
s3 = s1 | s2
print(s3) #  {'6', '9', '8', '5', '4', '2', '3', '7', '1'}
#%%

s4 = s1.union(s2)
print(s4) # {'6', '9', '8', '5', '4', '2', '3', '7', '1'}

